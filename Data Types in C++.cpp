// C++ Program to demonstrate different data types
#include <iostream>
#include <string>
using namespace std;

int main() {
    int age = 20;
    float height = 5.11f;
    double weight = 84.4;
    char grade = 'A';
    bool isStudent = true;
    string name = "Abhinav";

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Weight: " << weight << endl;
    cout << "Grade: " << grade << endl;
    cout << "Is Student: " << isStudent << endl;

    return 0;
}
/*  OUTPUT:
Name: Abhinav
Age: 20
Height: 5.11
Weight: 84.4
Grade: A
Is Student: 1
*/
